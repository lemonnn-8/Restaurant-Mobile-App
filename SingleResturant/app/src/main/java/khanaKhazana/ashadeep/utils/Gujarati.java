package khanaKhazana.ashadeep.utils;

public class Gujarati {
	String id, subname, image;

	String getId() {
		return id;
	}

	void setId(String id) {
		this.id = id;
	}

	String getImage() {
		return image;
	}

	String getSubname() {
		return subname;
	}

	void setSubname(String subname) {
		this.subname = subname;
	}

	void setImage(String image) {
		this.image = image;
	}
}
